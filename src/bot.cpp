double bot::minimax(state_t &s, int d, double alpha, double beta, int &visited) {
    visited++;
    vec<act_t> moves=actions(s, true);
    sort(all(moves),[s](act_t a, act_t b){return (s.at(a.dst)!='.') > (s.at(b.dst)!='.');});

    // leaf
    if (d==0 || abs(static_eval(s))>=1e9) return static_eval(s);

    if (s.turn==0) {
        // white
        double mx=-1e9;
        for (act_t &a : moves) {
            state_t sp=s;
            sp.move(a);
            double eval=minimax(sp, d-1, alpha, beta, visited);

            ckmax(mx, eval);

            ckmax(alpha, eval);
            if (beta<=alpha) break;
        }

        return mx;
    } else {
        // black
        double mn=1e9;
        for (act_t &a : moves) {
            state_t sp=s;
            sp.move(a);
            double eval=minimax(sp, d-1, alpha, beta, visited);

            ckmin(mn, eval);

            ckmin(beta, eval);
            if (beta<=alpha) break;
        }

        return mn;
    }
}

act_t bot::best_move(state_t &s, int depth) {
    vec<act_t> moves=actions(s, true);

    auto st=chrono::steady_clock::now();

    act_t best;
    double best_ev;
    int vis=0;

    if (s.turn==0) {
        double mx=-1e9;
        for (act_t a : moves) {
            state_t sp=s;
            sp.move(a);
            if (ckmax(mx, minimax(sp, depth, -1e9, 1e9, vis))) {
                best=a;
                best_ev=mx;
            }
        }
    } else {
        double mn=1e9;
        for (act_t a : moves) {
            state_t sp=s;
            sp.move(a);
            if (ckmin(mn, minimax(sp, depth, -1e9, 1e9, vis))) {
                best=a;
                best_ev=mn;
            }
        }
    }

    int dur=chrono::duration_cast<chrono::milliseconds>(chrono::steady_clock::now()-st).count();

    printf("visited %d states, took %.2fs\n", vis, (double)dur/1000);
    printf("%s going for best eval of %.2f\n", s.turn==0?"white":"black", best_ev);
    return best;
}
